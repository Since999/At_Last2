class CTopViewCamera : public CCamera
{
	XMFLOAT3 dir;
	float distance;

public:
	CTopViewCamera(CCamera* pCamera);
	virtual ~CTopViewCamera() { }

	virtual void Update(XMFLOAT3& xmf3LookAt, float fTimeElapsed);
	virtual void SetLookAt(XMFLOAT3& vLookAt);
	virtual void AddDistance(float distance_value) { 
		distance += distance_value; 
		if (distance < 100.0f)
			distance = 100.0f;
	}
};

